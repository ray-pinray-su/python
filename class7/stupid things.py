# 質數判斷程式
# 質數是只能被1和自己整除的數字
x = int(float(input("請輸入數字:")))  # 取得使用者輸入的數字
y = 0  # 計算因數個數
for i in range(1, x + 1):
    if x % i == 0:
        y += 1  # 因數個數加1
if y == 2:
    print("質數")  # 只有1和自己兩個因數
else:
    print("不是質數")  # 有其他因數


# 飲料販賣機
# 這個程式會根據使用者輸入的編號顯示對應的飲料
x = input("1.蘋果汁2.柳橙汁3.葡萄汁4.系統關閉" "請輸入編號:")
while True:
    if x == "1":
        print("商品是蘋果汁")
    elif x == "2":
        print("商品是柳橙汁")
    elif x == "3":
        print("商品是葡萄汁")
    elif x == "4":
        break  # 跳出迴圈
    else:
        print("無此選項,請重新輸入")
    x = input("1.蘋果汁2.柳橙汁3.葡萄汁4.系統關閉" "請輸入編號:")

print("系統關閉")


# 跳繩計時器
# 這個程式會計算跳繩次數，每10次提醒休息一下
jump = int(input("請輸入要跳繩的次數:"))  # 取得使用者輸入的跳繩次數
for i in range(1, jump + 1):
    if i % 10 == 0:
        print("休息一下吧!")  # 每10次跳繩提醒休息
        continue  # 跳過本次迴圈，執行下一次
    print(f"第{i}次跳繩")  # 列印目前的跳繩次數
