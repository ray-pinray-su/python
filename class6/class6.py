# while迴圈範例
# while迴圈會重複執行，直到條件為False才會停止
i = 1
while i < 10:
    print(i)  # 列印迴圈變數
    i += 1  # 每次迴圈加1

# 算術指定運算子
# 算術指定運算子可以簡化程式碼，將運算結果直接指派給變數
# +=  -=  *=  /=  //=  **=  %=
# x=x+5  等同於 x+=5
# x=x-5  等同於 x-=5
# x=x*5  等同於 x*=5
# x=x/5  等同於 x/=5
# x=x//5 等同於 x//=5
# x=x**5 等同於 x**=5
# x=x%5  等同於 x%=5


# 符號優先順序
# 運算子執行的順序從上到下依次執行
# 1.() 先執行括號內的運算
# 2.** 指數
# 3.+ - 正負號
# 4.* / // % 乘除,取餘數,整數除法
# 5.+ - 加減
# 6. > < >= <= == != 比較運算
# 7.and or not 邏輯運算
# 8.= += -= *= /= %= //= **= 指派運算


# while迴圈範例：列印1到9
i = 1
while i <= 9:
    print(i)
    i += 1  # 等於i=i+1，每次迴圈加1


# 密碼門練習
# 這個程式會要求使用者輸入密碼，最多允許3次失誤
password = input("請輸入密碼:")
number = 1  # 計算輸入次數
while password != "1234":
    print("密碼錯誤,請重新輸入")
    password = input("請輸入密碼:")
    number += 1  # 輸入次數加1
    if number > 3:  # 如果輸入超過3次
        print("密碼錯誤次數過多,請稍後再試")
        break  # 跳出迴圈
if password == "1234":
    print("密碼正確,歡迎使用本系統")
